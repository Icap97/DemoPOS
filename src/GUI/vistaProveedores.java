    public static DefaultTableModel modelo;
     private void CrearModelo(){
         try {
             modelo = (new DefaultTableModel(
                null, new String [] {
                "idProveedor","Nombre","NIT","Telefono","Direccion"}){
                Class[] types = new Class [] {
                java.lang.Object.class,
                java.lang.Object.class,
                java.lang.Object.class,
                java.lang.Object.class,
                java.lang.Object.class
                };
                boolean[] canEdit = new boolean [] {
                false,false,false,false,false
                };
                @Override
                public Class getColumnClass(int columnIndex) {
                   return types [columnIndex];
                }
                @Override
                public boolean isCellEditable(int rowIndex, int colIndex){
                   return canEdit [colIndex];
                }
            });
          jTable1.setModel(modelo);
         } catch (Exception e) {
             JOptionPane.showMessageDialog(null,e.toString()+"error");
            }    
        }
    private void Llenar_Tabla(){
        try {
            Object A[] = null;
            List<Proveedor>listaProveedores;
            listaProveedores = controladorProveedores.findProveedorEntities();
            for (int i = 0; i < listaProveedores.size(); i++) {
              modelo.addRow(A);
              modelo.setValueAt(listaProveedores.get(i).getIdProveedor(), i, 0);
              modelo.setValueAt(listaProveedores.get(i).getNombreProveedor(), i, 1);
              modelo.setValueAt(listaProveedores.get(i).getNitProveedor(), i, 2);
              modelo.setValueAt(listaProveedores.get(i).getTelProveedor(), i, 3);
              modelo.setValueAt(listaProveedores.get(i).getDirProveedor(), i, 4);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void limpiarTabla(){
        for (int i = 0; i < jTable1.getRowCount(); i++) {
        modelo.removeRow(i);
        i-=1;
        }
        jTable1.setModel(new DefaultTableModel());
        
    }

    private void eliminar(){
    
        try {
            
            int sel = jTable1.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int dato = (int) model.getValueAt(sel, 0);
            controladorProveedores.destroy(dato);
                       
        } catch (IllegalOrphanException ex) {
            Logger.getLogger(vistaEmpleados.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NonexistentEntityException ex) {
            Logger.getLogger(vistaEmpleados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

